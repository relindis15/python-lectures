# collect two numbers from a user and perform some arithmetic operators for these numbers

# Input 1st number
num1 = int(input("Enter 1st number: "))

# Input 2nd  number
num2 = int(input("Enter 2nd number: "))

# Print the sum of 1st and 2nd number
addition = num1 + num2
print("The sum of", num1, "and", num2, "is:", addition)

# Print the difference of 1st and 2nd number
subtraction = num1 - num2
print("The difference of", num1, "and", num2, "is:", subtraction)

# Print the product of 1st and 2nd number
multiplication = num1 * num2
print("The product of", num1, "and", num2, "is:", multiplication)

# Print the quotient of 1st and 2nd number
division = num1 // num2
print("The quotient of", num1, "and", num2, "is:", division)
