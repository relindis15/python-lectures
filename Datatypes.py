# data types in python
# x ,y , z =10,20,30 # a simple way to declare variables on the same line
# strings
name = "Relindis"
print(name)
# to get the datatype for the above use the type function
print(type(name))

# integer
age = 34
print(type(age))

# float
mark = 19.03
print(type(mark))

# list
ages = [1, 2, 3, 5]
print(type(ages))

# dictionaries
student = {
    "name": "Relindis",
    "age": 16,
    "address": "USA"
}
print(student)
